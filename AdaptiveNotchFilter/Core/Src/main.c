/* USER CODE BEGIN 4 */
void BlockProcess(uint32_t start, uint32_t end) {
	// Input Buffer Transfer to uint32_t
	for (uint32_t i = start; i < end; i++) {
		ADC_inp[i] = inputBuffer[i].inp;
		ADC_ref[i] = inputBuffer[i].ref;
	}

	// Filtering
	ANF_BlockFilter_dbl(&anf, &ADC_ref[start], &DAC_err[start], halfBUF_SIZE, ADAPT, an, LAMBDA);
	ANF_BlockFilter_dbl(&nf,  &ADC_inp[start], &DAC_out[start], halfBUF_SIZE, FEED,  an, 0);

#ifdef LOG
	ADC_input = ADC_inp[0];
	ADC_reference = ADC_ref[0];
	DAC_error = DAC_err[0];
	DAC_output = DAC_out[0];
	fn = (int32_t)(acos(an[0]/2)/(2*M_PI/FS));
#endif
}


